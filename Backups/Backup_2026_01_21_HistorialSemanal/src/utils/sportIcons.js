/**
 * Obtener icono de un deporte
 * @param {string} sportKey 
 */
export function getSportIcon(sportKey) {
    return SPORT_ICONS[sportKey] || 'sports';
}

/**
 * Obtener nombre legible en ESPAÑOL
 * @param {string} sportKey 
 */
export function getSportDisplayName(sportKey) {
    return SPORT_NAMES_ES[sportKey] || sportKey.split('_').map(w => w.charAt(0).toUpperCase() + w.slice(1)).join(' ');
}

/**
 * Obtener lista completa para el selector
 */
export function getAllSports() {
    return Object.keys(SPORT_ICONS).map(key => ({
        key: key,
        name: getSportDisplayName(key), // Ahora devuelve español
        icon: getSportIcon(key),
        searchTerms: `${key} ${getSportDisplayName(key)}`.toLowerCase() // Para buscar en inglés y español
    })).sort((a, b) => a.name.localeCompare(b.name));
}
